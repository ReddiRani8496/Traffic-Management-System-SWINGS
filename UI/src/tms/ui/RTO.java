    private void RegisterBtnActionPerformed(java.awt.event.ActionEvent evt) {
        if (AppNoField.getText().isEmpty() || VehNumField.getText().isEmpty() || 
            VehIDField.getText().isEmpty() || OwnerIdField.getText().isEmpty() || 
            DateOfPurchaseField.getDate() == null || DistributeNameField.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Please enter all the fields", "Incomplete Information", JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        int appNo = Integer.parseInt(AppNoField.getText());
        String vehNo = VehNumField.getText();
        int vehId = Integer.parseInt(VehIDField.getText());
        int ownerId = Integer.parseInt(OwnerIdField.getText());
        java.util.Date purchaseDate = DateOfPurchaseField.getDate();
        java.sql.Date purchase = new java.sql.Date(purchaseDate.getTime());
        String distributer = DistributeNameField.getText();
        
        // Validate vehicle ID
        if (!validate.isValidVehId(vehId)) {
            JOptionPane.showMessageDialog(null, "Invalid vehicle ID.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        // Validate owner ID
        if (!validate.isValidOwnerId(ownerId)) {
            JOptionPane.showMessageDialog(null, "Invalid owner ID.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        RegDetailsEO reg = new RegDetailsEO();
        reg.setAppNo(appNo);
        reg.setVehNo(vehNo);
        VehicleDetailsEO vehicle = new VehicleDetailsEO();
        vehicle.setVehId(vehId);
        OwnerDetailsEO owner = new OwnerDetailsEO();
        owner.setOwnerId(ownerId);
        reg.setOwnerDetails(owner);
        reg.setVehicleDetails(vehicle);
        reg.setDistributerName(distributer);
        reg.setDateOfPurchase(purchase);
        
        RTOOperations rto = new RTOOperationsImpl();
        int inserted = rto.transferVehicle(reg);
        
        if (inserted > 0) {
            JOptionPane.showMessageDialog(null, "Vehicle registered successfully", "Success", JOptionPane.INFORMATION_MESSAGE);
            AppNoField.setText("");
            VehNumField.setText("");
            VehIDField.setText("");
            OwnerIdField.setText("");
            DateOfPurchaseField.setDate(null);
            DistributeNameField.setText("");
        } else {
            JOptionPane.showMessageDialog(null, "Failed to register vehicle", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }

        
