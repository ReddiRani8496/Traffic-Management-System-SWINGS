    private void CreateUserBtnActionPerformed(java.awt.event.ActionEvent evt) {                                          
        String userName = UsernameFieldInCreateUser.getText();
        String updatedPassword = new String(PswdFieldInCreateUser.getPassword());
        String updatedRole = (String) RoleComboInCreateUser.getSelectedItem();

        if (userName.isEmpty() || updatedPassword.isEmpty() || updatedRole.isEmpty()) {
            increaseDialogSize();
            JOptionPane.showMessageDialog(this, "Please fill in all fields.", "Incomplete Information", JOptionPane.ERROR_MESSAGE);
        } else {
            // Validate username uniqueness
            if (!validate.isUniqueUser(userName)) {
                JOptionPane.showMessageDialog(this, "Username already exists. Please choose a different username.", "Invalid Input", JOptionPane.WARNING_MESSAGE);
                return;
            }

            // Validate username format
            if (!validate.validateName(userName)) {
                JOptionPane.showMessageDialog(this, "Invalid username. Please enter a valid name.", "Invalid Input", JOptionPane.WARNING_MESSAGE);
                return;
            }

            // Validate password format
            if (!validate.validatePassword(updatedPassword)) {
                JOptionPane.showMessageDialog(this, "Invalid password. Password must be strong with alphabets, caps, special symbols, numbers, and minimum 8 characters.", "Invalid Input", JOptionPane.WARNING_MESSAGE);
                return; 
            }

            // If all validations pass, proceed to create the user
            UserMasterEO user = new UserMasterEO();
            user.setUsername(userName);
            user.setPassword(updatedPassword);
            RoleMasterEO role = new RoleMasterEO();
            role.setRoleName(updatedRole);
            user.setRole(role);

            AdminOperations admin = new AdminOperationsImpl();
            admin.addNewUser(user);

            JOptionPane.showMessageDialog(this, "User created successfully.", "User Created", JOptionPane.INFORMATION_MESSAGE);
            UsernameFieldInCreateUser.setText("");
            PswdFieldInCreateUser.setText("");
            RoleComboInCreateUser.setSelectedIndex(0);
        }
    }

