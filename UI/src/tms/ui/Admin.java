    private void populateRoleNames() {
        RoleMasterDAO roleMasterDAORef = RoleMasterDAOFactory.createRoleMasterDAO();
        List<RoleMasterEO> roleNames = roleMasterDAORef.findAllRoles();

        UserRoleCombo.removeAllItems();
        RoleComboInCreateUser.removeAllItems();
        DeleteRoleNameCombo.removeAllItems();

        for (RoleMasterEO role : roleNames) {
            UserRoleCombo.addItem(role.getRoleName());
            RoleComboInCreateUser.addItem(role.getRoleName());
            DeleteRoleNameCombo.addItem(role.getRoleName());
        }
    }


